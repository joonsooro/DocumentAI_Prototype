// 4. Boot the sidecar dev-agent-server
// ---------------------------------------------------------------------------
function bootServer() {
  return new Promise((resolveBoot, rejectBoot) => {
    const env = { ...process.env, AGENT_SERVER_PORT: String(PORT) };
    const child = spawn(
      'npx',
      ['--yes', 'tsx', '--env-file=.env', 'scripts/dev-agent-server.ts'],
      { cwd: REPO_ROOT, env, stdio: ['ignore', 'pipe', 'pipe'] },
    );

    let booted = false;
    const log = [];
    const onData = (buf) => {
      const s = buf.toString();
      log.push(s);
      if (!booted && s.includes('[dev-agent-server] listening')) {
        booted = true;
        resolveBoot({ child, log });
      }
    };
    child.stdout.on('data', onData);
    child.stderr.on('data', onData);
    child.on('exit', (code, signal) => {
      if (!booted) {
        rejectBoot(
          new Error(
            `dev-agent-server exited before boot (code=${code} signal=${signal}). ` +
              `Log tail:\n${log.join('').slice(-1500)}`,
          ),
        );
      }
    });
    setTimeout(() => {
      if (!booted) {
        try { child.kill('SIGKILL'); } catch { /* noop */ }
        rejectBoot(
          new Error(`dev-agent-server did not boot within 30s. Log:\n${log.join('').slice(-1500)}`),
        );
      }
    }, 30_000);
  });
}

